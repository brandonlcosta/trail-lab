const progressBar = document.querySelector("#progressBar");
const railLinks = [...document.querySelectorAll(".progress-rail a")];
const sections = railLinks.map((link) => document.querySelector(link.getAttribute("href"))).filter(Boolean);

function updateProgress() {
  const scrollable = document.documentElement.scrollHeight - window.innerHeight;
  const percent = scrollable > 0 ? Math.min((window.scrollY / scrollable) * 100, 100) : 0;
  progressBar.style.width = `${percent}%`;

  let activeId = sections[0]?.id;
  for (const section of sections) {
    if (section.getBoundingClientRect().top < window.innerHeight * 0.36) {
      activeId = section.id;
    }
  }

  railLinks.forEach((link) => {
    const isActive = link.dataset.target === activeId;
    link.classList.toggle("is-active", isActive);
    link.setAttribute("aria-current", isActive ? "true" : "false");
  });
}

window.addEventListener("scroll", updateProgress, { passive: true });
window.addEventListener("resize", updateProgress);
updateProgress();
