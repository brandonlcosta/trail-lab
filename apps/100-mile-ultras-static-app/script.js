function setupProgress() {
  const fill = document.querySelector(".progress-fill");
  const active = document.querySelector(".active-section");
  const sections = [...document.querySelectorAll("[data-section-label]")];

  const update = () => {
    const scrollTop = window.scrollY;
    const docHeight = document.documentElement.scrollHeight - window.innerHeight;
    const percent = docHeight > 0 ? Math.min(100, Math.max(0, (scrollTop / docHeight) * 100)) : 0;
    fill.style.width = `${percent}%`;

    const current = sections
      .map((section) => ({
        label: section.dataset.sectionLabel,
        distance: Math.abs(section.getBoundingClientRect().top - 92)
      }))
      .sort((a, b) => a.distance - b.distance)[0];

    if (current) active.textContent = current.label;
  };

  update();
  window.addEventListener("scroll", update, { passive: true });
  window.addEventListener("resize", update);
}

function setupReveal() {
  const items = document.querySelectorAll(".reveal");

  if (!("IntersectionObserver" in window)) {
    items.forEach((item) => item.classList.add("is-visible"));
    return;
  }

  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add("is-visible");
          observer.unobserve(entry.target);
        }
      });
    },
    { threshold: 0.08, rootMargin: "0px 0px -40px 0px" }
  );

  items.forEach((item) => observer.observe(item));
}

