const progressFill = document.querySelector(".progress-track span");
const progressLinks = [...document.querySelectorAll(".progress-rail a")];
const sections = [...document.querySelectorAll(".section")];

function updateProgress() {
  const scrollTop = window.scrollY;
  const docHeight = document.documentElement.scrollHeight - window.innerHeight;
  const amount = docHeight > 0 ? Math.min(1, scrollTop / docHeight) : 0;
  progressFill.style.height = `${Math.max(6, amount * 100)}%`;

  let activeId = "top";
  sections.forEach((section) => {
    if (section.offsetTop <= scrollTop + window.innerHeight * 0.38) {
      activeId = section.id || section.closest("[id]")?.id || activeId;
    }
  });

  progressLinks.forEach((link) => {
    link.classList.toggle("is-active", link.dataset.target === activeId);
  });
}

const revealObserver = new IntersectionObserver(
  (entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        entry.target.classList.add("is-visible");
        revealObserver.unobserve(entry.target);
      }
    });
  },
  { threshold: 0.12 }
);

document
  .querySelectorAll(".summary-card, .utility-card, .section-block, .example-card, .warning-card, .framework-card, .worksheet-card, .footer-card")
  .forEach((item) => revealObserver.observe(item));

window.addEventListener("scroll", updateProgress, { passive: true });
window.addEventListener("resize", updateProgress);
updateProgress();
